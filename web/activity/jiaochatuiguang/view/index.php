$(this).ready(function(){

	//用图片替代数字
	function to_img (number){
		var number=number;
		var imgs=['0.png','1.png','2.png','3.png','4.png','5.png','6.png','7.png','8.png','9.png'];
		var ns='0123456789'.split('');
		var output='';
		var number=number+'';
		for(var i=0;i<number.length;i++){
			for(var j=0;j<ns.length;j++){
				if(number[i]==ns[j]){
				output+="<img src=web/activity/jiaochatuiguang/image/"+imgs[j]+">";
				break;
		   		}
		  	}
		}
		return output;
	}
	
	//按钮按下效果
	$(".getreward").mousedown(function(){
		var id = $(this).attr("id");
		$(this).css("background-image","url(web/activity/jiaochatuiguang/image/"+id+"-on.png)");
	});
	
	$(".getreward").mouseup(function(){
		var id = $(this).attr("id");
		$(this).css("background-image","url(web/activity/jiaochatuiguang/image/"+id+"-leave.png)");
	});

	//区分平台
	var ctype = <?php echo $ctype?>;
	if(ctype == 1){
	    $(".ctypechange").attr("src","web/activity/jiaochatuiguang/image/sh-android.png")
	}

    var gameid = <?php echo $gameid?>;
    var paijuFrequency1 = <?php echo $paijuFrequency1?>;
    var paijuFrequency3 = <?php echo $paijuFrequency3?>;
    var paijuFrequency4 = <?php echo $paijuFrequency4?>;
    var flag1 = <?php echo $flag1?>;
    var flag3 = <?php echo $flag3?>;
    var flag4 = <?php echo $flag4?>;
    

	//区分游戏
	if(gameid == 1){
		$("#landlord").show();
		$("#bullfight").show();
	}else if(gameid == 3){
		$("#showhand").show();
		$("#bullfight").show();
	}else if(gameid == 4){
		$("#showhand").show();
		$("#landlord").show();
	}

	//完成进度
    function cover(flag1,flag3,flag4,paijuFrequency1,paijuFrequency3,paijuFrequency4){
		if(flag1 == 0 && paijuFrequency1<10){
            $("#shcover").show();
            var number = to_img(paijuFrequency1);
			$("#shnumber").html(number);
        }else if(flag1 == 1){
        	$("#shhave").show();
        }
		if(flag3 == 0 && paijuFrequency3<10){
            $("#llcover").show();
            var number = to_img(paijuFrequency3);
			$("#llnumber").html(number);
        }else if(flag3 == 1){
        	$("#llhave").show();
        }
		if(flag4 == 0 && paijuFrequency4<10){
		    $("#bfcover").show();
		    var number = to_img(paijuFrequency4);
			$("#bfnumber").html(number);
        }else if(flag4 == 1){
        	$("#bfhave").show();
        }
    }

    cover(flag1,flag3,flag4,paijuFrequency1,paijuFrequency3,paijuFrequency4);
	
    //领取奖励
    var which = 0;

    $(".getreward").mouseup(function(){
        var id = $(this).attr("id");
        if(id == 'sh'){
            which = 1;
        }else if(id == 'll'){
            which = 3;
        }else if(id == 'bf'){
        	which = 4;
        }
        $.ajax({
            type:"POST",
            url:"<?php echo $url ?>?m=activity&p=dispath&fpath=jiaochatuiguang&act=set",
            data:"gameid=<?php echo $gameid?>"
				+"&mid=<?php echo $mid?>"
				+"&sid=<?php echo $sid?>"
				+"&cid=<?php echo $cid?>"
				+"&pid=<?php echo $pid?>"
				+"&ctype=<?php echo $ctype?>"
                +"&which="+which,
            dataType:"json",
            success:function(e){
                var paijuFrequency1 = e.paijuFrequency1;
                var paijuFrequency3 = e.paijuFrequency3;
                var paijuFrequency4 = e.paijuFrequency4;
                var flag1           = e.flag1;
                var flag3           = e.flag3;
                var flag4           = e.flag4;
                var status          = e.status;

                if(status == 0 || status == 1){
                	cover(flag1,flag3,flag4,paijuFrequency1,paijuFrequency3,paijuFrequency4);
                	$("#message").text("恭喜您获得1888金币!");
					$("#mess").stop(true,true).fadeIn(100);
					$("#mess").stop(true,true).fadeOut(3000);
                }else {
                	$("#message").text("领取失败("+status+")");
					$("#mess").stop(true,true).fadeIn(100);
					$("#mess").stop(true,true).fadeOut(3000);
                }
            }
        });
    });
});
